package org.etudes.ambrosia.api;

/**
 * A GridRow is one row of columns in a Grid.
 */
public interface GridRow
{
	/**
	 * Access the 1st column
	 * 
	 * @return The Object at the 1st column.
	 */
	public Object getCol01();

	/**
	 * Access the 2nd column
	 * 
	 * @return The Object at the 2nd column.
	 */
	public Object getCol02();

	/**
	 * Access the 3rd column
	 * 
	 * @return The Object at the 3rd column.
	 */
	public Object getCol03();

	/**
	 * Access the 4th column
	 * 
	 * @return The Object at the 4th column.
	 */
	public Object getCol04();

	/**
	 * Access the 5th column
	 * 
	 * @return The Object at the 5th column.
	 */
	public Object getCol05();

	/**
	 * Access the 6th column
	 * 
	 * @return The Object at the 6th column.
	 */
	public Object getCol06();

	/**
	 * Access the 7th column
	 * 
	 * @return The Object at the 7th column.
	 */
	public Object getCol07();

	/**
	 * Access the 8th column
	 * 
	 * @return The Object at the 8th column.
	 */
	public Object getCol08();

	/**
	 * Access the 9th column
	 * 
	 * @return The Object at the 9th column.
	 */
	public Object getCol09();
}
